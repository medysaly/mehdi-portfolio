export default function Sidebar() {
  const [active, setActive] = useState<string>("about");

  useEffect(() => {
    const handleScroll = () => {
      // At the bottom of the page, force the last section to be active.
      // Short trailing sections may never span the detection line otherwise.
      const scrollBottom = window.scrollY + window.innerHeight;
      const nearBottom = scrollBottom >= document.documentElement.scrollHeight - 80;
      if (nearBottom) {
        setActive(nav[nav.length - 1].href.slice(1));
        return;
      }

      const sections = nav.map((n) => n.href.slice(1));
      const current = sections.find((id) => {
        const el = document.getElementById(id);
        if (!el) return false;
        const rect = el.getBoundingClientRect();
        return rect.top <= 200 && rect.bottom > 200;
      });
      if (current) setActive(current);
    };

    handleScroll();
    window.addEventListener("scroll", handleScroll, { passive: true });
    return () => window.removeEventListener("scroll", handleScroll);
  }, []);

  return (
    <aside className="lg:sticky lg:top-0 lg:flex lg:h-screen lg:flex-col lg:justify-between lg:py-24">
      <div>
        <motion.div
          initial={{ opacity: 0, y: 12 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.6 }}
        >
          <h1 className="font-display text-5xl font-medium tracking-tight text-white lg:text-6xl">
            Mehdi Salhi
          </h1>
          <p className="mt-3 font-display text-lg text-neutral-300">
            AWS Cloud Engineer
          </p>
          <p className="mt-4 max-w-xs font-body text-sm leading-relaxed text-neutral-500">
            Computer Science student graduating August 2026 (SNHU). Building
            serverless apps and AI agents on AWS.
          </p>

          <div className="mt-6 inline-flex items-center gap-2 rounded-full border border-white/[0.1] bg-white/[0.02] px-3 py-1 text-xs text-neutral-400">
            <span className="relative flex h-1.5 w-1.5">
              <span className="absolute inline-flex h-full w-full animate-ping rounded-full bg-green-400 opacity-75" />
              <span className="relative inline-flex h-1.5 w-1.5 rounded-full bg-green-400" />
            </span>
            Open to work
          </div>
        </motion.div>

        {/* Nav — desktop only */}
        <motion.nav
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ duration: 0.6, delay: 0.2 }}
          className="mt-16 hidden lg:block"
          aria-label="Section navigation"
        >
          <ul className="space-y-3">
            {nav.map((item) => {
              const id = item.href.slice(1);
              const isActive = active === id;
              return (
                <li key={item.href}>
                  <a
                    href={item.href}
                    className={`group flex items-center gap-4 py-1 text-sm transition-colors ${
                      isActive
                        ? "text-white"
                        : "text-neutral-500 hover:text-neutral-200"
                    }`}
                  >
                    <span
                      className={`h-px transition-all duration-300 ${
                        isActive
                          ? "w-12 bg-white"
                          : "w-6 bg-neutral-700 group-hover:w-10 group-hover:bg-neutral-400"
                      }`}
                    />
                    <span
                      className={`uppercase tracking-[0.2em] text-[11px] font-medium ${
                        isActive ? "" : ""
                      }`}
                    >
                      {item.label}
                    </span>
                  </a>
                </li>
              );
            })}
          </ul>
        </motion.nav>
      </div>

      {/* Socials */}
      <motion.div
        initial={{ opacity: 0 }}
        animate={{ opacity: 1 }}
        transition={{ duration: 0.6, delay: 0.4 }}
        className="mt-12 flex items-center gap-5 lg:mt-0"
      >
        {socials.map((social) => (
          <a
            key={social.label}
            href={social.href}
            target={social.label === "Email" ? undefined : "_blank"}
            rel={social.label === "Email" ? undefined : "noopener noreferrer"}
            aria-label={social.label}
            className="text-neutral-500 transition-colors hover:text-white"
          >
            {social.icon}
          </a>
        ))}
      </motion.div>
    </aside>
  );
}
